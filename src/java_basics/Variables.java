package java_basics;

public class Variables {
	
	public static void main(String [] args) {
		
		// variables are a placeholder for data, need to be declare with a data type, names must start with a letter
		// = is the assignment operator (assign what is one right to what is on left)
		
		int x;
		x = 4;
		
		int y = 5;
		
		System.out.println(x);
		
		
		
		// String = data type for storing sequence of characters / words
		
		String words;
		words = "Hello World";
		
		// + operator for String - concatenates a string
		
		System.out.println(words + ", this is a java class");
		
	}

}
